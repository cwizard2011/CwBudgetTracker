const dictionaries: Record<string, Dictionary> = { en, pt, fr, es, de };

export function translate(locale: string, key: string, params?: Record<string, string | number>): string {
  const loc = (locale || 'en').toLowerCase();
  let lang = 'en';
  if (loc.startsWith('pt')) lang = 'pt';
  else if (loc.startsWith('fr')) lang = 'fr';
  else if (loc.startsWith('es')) lang = 'es';
  else if (loc.startsWith('de')) lang = 'de';
  const dict = dictionaries[lang] || en;
  let value = dict[key] || en[key] || key;
  if (params) {
    Object.keys(params).forEach(k => {
      value = value.replace(new RegExp(`{${k}}`, 'g'), String(params[k]));
    });
  }
  return value;
}

export function useI18n() {
  const { locale } = useSettings();
  return useCallback(
    (key: string, params?: Record<string, string | number>) => translate(locale, key, params),
    [locale],
  );
}


